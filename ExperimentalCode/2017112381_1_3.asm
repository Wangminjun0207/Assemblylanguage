;点亮与8255相连的PC0-PC7八个发光二极管
IOYO      EQU 0600H
MY8255_A  EQU IOYO+00H*2
MY8255_B  EQU IOYO+01H*2
MY8255_C  EQU IOYO+02H*2
MY8255_MODE  EQU IOYO+03H*2
SSTACK SEGMENT STACK
	DW 32 DUP(?)
SSTACK ENDS
CODE SEGMENT
	ASSUME CS:CODE
START:
	MOV DX, MY8255_MODE
	MOV AL, 80H
	OUT DX, AL
AA1:
	MOV DX, MY8255_B
	MOV AL, 0FFH
	OUT DX, AL
	CALL DELAY
	JMP AA1
DELAY:
	PUSH CX
	MOV CX, 0F00H
AA2:
	PUSH AX
	POP AX
	LOOP AA2
	POP CX
	RET
CODE ENDS
	END START