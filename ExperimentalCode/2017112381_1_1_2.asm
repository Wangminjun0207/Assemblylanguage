;编程将0000:3500H开始的单元初始化为自己的学号
;将0000:3500H开始的单元复制到0000:3600H开始的单元
SSTACK SEGMENT STACK
	DW 32 DUP(?)
SSTACK ENDS

CODE SEGMENT
	ASSUME SS:SSTACK, CS:CODE
START:
	PUSH DS
	MOV AX, 0
	MOV DS, AX
	MOV CX, 10
	MOV DI, 3500H
	MOV SI, 3600H
INIT:
	MOV [DI], 2
	INC DI
	MOV [DI], 0
	INC DI
	MOV [DI], 1
	INC DI
	MOV [DI], 7
	INC DI
	MOV [DI], 1
	INC DI
	MOV [DI], 1
	INC DI
	MOV [DI], 2
	INC DI
	MOV [DI], 3
	INC DI
	MOV [DI], 8
	INC DI
	MOV [DI], 1
	INC DI
	MOV DI, 3500H
NEXT:
	MOV AL, [DI]
	MOV [SI], AL
	INC SI
	INC DI
	LOOP NEXT
EXIT:
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START
	