;编程将00F-0FF单元的数据写到内存0000:3000H开始的单元
SSTACK SEGMENT STACK
	DW 32 DUP(?)
SSTACK ENDS

CODE SEGMENT
	ASSUME SS:SSTACK, CS:CODE
START:
	PUSH DS
	MOV AX, 0
	MOV DS, AX
	MOV CX, 16
	MOV DI, 0000H
	MOV SI, 3000H
NEXT:
	MOV AL, [DI]
	MOV [SI], AL
	INC SI
	INC DI
	LOOP NEXT
EXIT:
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START
	